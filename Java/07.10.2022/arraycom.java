import java.util.*;
class arraycom 
{
	static Scanner s = new Scanner(System.in);
	public static void main(String[] args) 
	{
		int [] a = {10,9,8,7,6};
		int [] b = {1,2,3,4,5};
		for (int i = 0;i<a.length;i++)
		{
			System.out.print(a[i]+" ");
		}
		for (int j = 0;j<b.length;j++)
		{
			System.out.print(b[j]+" ");
		}
	}
}
