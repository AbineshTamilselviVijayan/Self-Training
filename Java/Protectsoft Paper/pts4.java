import java.util.*;
class pts4 
{
	static Scanner s = new Scanner(System.in);
	public static void main(String[] args) 
	{
		System.out.println("Enter n");
		int n = s.nextInt();
		System.out.println("Enter i");
		int i = s.nextInt();
		for (int j = n;j<=(i*n);j+=2)
		{
			System.out.println(j+" % "+n+" = "+(j/n));
		}
	}
}
